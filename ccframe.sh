is_interactive() {
  if [ -t 0 ]; then
    return 0
  fi
  if [ -r /dev/tty ]; then
    return 0
  fi
  return 1
}

detect_server_ip() {
  local ip
  ip=$(hostname -I 2>/dev/null | awk '{print $1}')
  if [ -z "${ip:-}" ]; then
    ip=$(curl -fsSL https://api.ipify.org 2>/dev/null || echo "127.0.0.1")
  fi
  echo "$ip"
}

set_env_value() {
  local key="$1"
  local value="$2"
  local sed_value
  sed_value=${value//\\/\\\\}
  sed_value=${sed_value//&/\\&}
  sed_value=${sed_value//#/\\#}
  if [ -f .env ] && grep -q "^${key}=" .env; then
    sed -i "s#^${key}=.*#${key}=${sed_value}#" .env
  else
    printf '%s=%s\n' "$key" "$value" >> .env
  fi
}

remove_env_key() {
  local key="$1"
  if [ -f .env ]; then
    sed -i "/^${key}=.*/d" .env
  fi
}

get_env_value() {
  local key="$1"
  if [ -f .env ]; then
    grep -m1 "^${key}=" .env | cut -d'=' -f2-
  fi
}

ensure_certbot_webroot() {
