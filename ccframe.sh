cmd_install() {
  check_system

  local USE_IMAGE=0
  for arg in "$@"; do
    case "$arg" in
      --from-image) USE_IMAGE=1 ;;
    esac
  done

  if [ "$USE_IMAGE" -eq 1 ]; then
    DEPLOYMENT_METHOD="image"
    print_info "使用镜像部署模式"

    PROJECT_DIR="/opt/ccframe"
    mkdir -p "$PROJECT_DIR"
    cd "$PROJECT_DIR" || { print_error "无法进入目录 $PROJECT_DIR"; exit 1; }

    prepare_image_compose
    ensure_env

    print_step "拉取最新镜像: $GHCR_IMAGE"
    if docker pull "$GHCR_IMAGE"; then
      print_success "镜像拉取成功"
    else
      print_error "镜像拉取失败"
      print_info "可能的原因："
      print_info "  1. 网络问题 - 请检查网络连接"
      print_info "  2. 镜像不存在 - 请确认镜像地址: $GHCR_IMAGE"
      print_info "  3. 认证问题 - 如果是私有镜像，需要先登录: docker login ghcr.io"
      exit 1
    fi
  else
    DEPLOYMENT_METHOD="source"
    print_info "使用源码构建模式"

    clone_project
    ensure_env

    local FORCE_FIX_COMPOSE=0
    for arg in "$@"; do
      case "$arg" in
        --fix-compose) FORCE_FIX_COMPOSE=1 ;;
      esac
    done

    ensure_compose_integrity "$FORCE_FIX_COMPOSE"
  fi

  print_step "正在清理旧容器与缓存..."
  $DOCKER_COMPOSE_CMD down --remove-orphans 2>/dev/null || true
  docker system prune -f --volumes >/dev/null 2>&1 || true
  docker network prune -f >/dev/null 2>&1 || true
  docker volume prune -f >/dev/null 2>&1 || true

  systemctl stop nginx 2>/dev/null || service nginx stop 2>/dev/null || true
  print_success "清理完成"

  print_step "正在启动容器..."
  if [ "$USE_IMAGE" -eq 1 ]; then
    $DOCKER_COMPOSE_CMD up -d
  else
    $DOCKER_COMPOSE_CMD up -d --build --force-recreate
  fi

  show_info
}

