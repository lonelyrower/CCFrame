configure_deployment_target() {
  local current_mode=$(get_env_value DEPLOYMENT_MODE)
  local choice="${current_mode:-}"

  if [ -n "$current_mode" ] && is_interactive; then
    read -rp "检测到现有部署模式 ($current_mode)，是否需要重新配置? [y/N]: " change
    case "${change,,}" in
      y|yes)
        choice=""
        ;;
    esac
  fi

  if [ -z "$choice" ]; then
    if is_interactive; then
      echo "请选择部署模式："
      echo "  1) IP 模式（仅 HTTP，使用服务器 IP 访问）"
      echo "  2) 域名模式（自动申请 Let's Encrypt HTTPS）"
      echo "  3) 域名 + 反向代理模式（HTTPS 由 Cloudflare/CDN 提供）"
      read -rp "请输入选项 [1/2/3] (默认 1): " choice
    else
      choice="1"
    fi
  fi

  choice=${choice:-1}

  case "$choice" in
    1|ip|http)
      configure_ip_mode
      ;;
    2|letsencrypt|https|domain)
      configure_letsencrypt_mode
      ;;
    3|cloudflare|cdn|proxy)
      configure_cloudflare_mode
      ;;
    *)
      print_warning "未知选项 $choice，自动使用 IP 模式"
      configure_ip_mode
      ;;
  esac
}

DOCKER_COMPOSE_CMD=""

check_system() {
  print_step "正在检查系统环境..."

  if [[ "${OSTYPE:-}" != linux* ]]; then
    print_error "此脚本仅支持 Linux 服务器（VPS）"
    exit 1
  fi
  print_success "检测到操作系统：Linux"

  if ! command -v git >/dev/null 2>&1 || ! command -v curl >/dev/null 2>&1; then
    print_info "正在安装 git/curl..."
    apt-get update -y >/dev/null 2>&1 || true
    apt-get install -y git curl >/dev/null 2>&1 || true
  fi
  print_success "git/curl 已就绪"

  if ! command -v docker >/dev/null 2>&1; then
    print_info "正在安装 Docker..."
    curl -fsSL https://get.docker.com | sh
  fi
  print_success "Docker 版本：$(docker --version | cut -d' ' -f3)"

  if docker compose version >/dev/null 2>&1; then
    DOCKER_COMPOSE_CMD="docker compose"
  elif command -v docker-compose >/dev/null 2>&1; then
    DOCKER_COMPOSE_CMD="docker-compose"
  else
    print_info "正在安装 Docker Compose 插件..."
    apt-get install -y docker-compose-plugin >/dev/null 2>&1 || true
    if docker compose version >/dev/null 2>&1; then
      DOCKER_COMPOSE_CMD="docker compose"
    else
      print_error "无法找到 Docker Compose 命令"
      exit 1
    fi
  fi
  print_success "Compose 命令：$DOCKER_COMPOSE_CMD"
}

clone_project() {
  print_step "正在获取项目代码..."

  PROJECT_DIR="/opt/ccframe"
  REPO_URL="https://github.com/lonelyrower/CCFrame.git"
  BRANCH="main"

  mkdir -p "$PROJECT_DIR"

  if [ -d "$PROJECT_DIR/.git" ]; then
    cd "$PROJECT_DIR" || { print_error "无法进入目录 $PROJECT_DIR"; exit 1; }
    print_info "正在更新已有仓库..."

    if git pull --rebase --autostash; then
      print_success "代码更新成功"
      return
    else
      print_warning "git pull 失败，尝试强制重置..."
      git fetch --all --prune || true
      if git reset --hard "origin/$BRANCH" 2>/dev/null; then
        print_success "已强制重置到最新代码"
        return
      else
        print_warning "更新失败，将重新克隆..."
        cd /
        rm -rf "$PROJECT_DIR"
        mkdir -p "$PROJECT_DIR"
      fi
    fi
  fi

  print_info "正在克隆仓库：$REPO_URL -> $PROJECT_DIR"
  if git clone --depth 1 --branch "$BRANCH" "$REPO_URL" "$PROJECT_DIR"; then
    cd "$PROJECT_DIR" || { print_error "无法进入目录 $PROJECT_DIR"; exit 1; }
    print_success "仓库克隆成功：$PROJECT_DIR"
    return
  fi

  print_error "克隆仓库失败：$REPO_URL"
  exit 1
}

prepare_image_compose() {
  print_step "生成镜像部署 docker-compose.yml..."

  cat > docker-compose.yml <<'EOF'
services:
  web:
    image: ghcr.io/lonelyrower/ccframe:${IMAGE_TAG:-latest}
    container_name: ccframe-web
    restart: unless-stopped
    ports:
      - "3000:3000"
    env_file:
      - .env
    environment:
      NODE_ENV: production
      NEXT_TELEMETRY_DISABLED: 1
    depends_on:
      db:
        condition: service_healthy
      redis:
        condition: service_started
      minio:
        condition: service_started
    command:
      - sh
      - -c
      - |
        set -e
        echo 'Applying database migrations...'
        for i in $(seq 1 60); do npx prisma migrate deploy && break || (echo 'DB not ready, retry...' && sleep 2); done
        npx prisma generate
        node scripts/create-admin.js || true
        echo 'Starting Next.js server on port 3000...'
        npm start
    healthcheck:
      test: ["CMD-SHELL", "curl -f http://localhost:3000/api/health-simple || exit 1"]
      interval: 30s
      timeout: 15s
      retries: 5
      start_period: 120s
    networks:
      - ccframe

  worker:
    image: ghcr.io/lonelyrower/ccframe:${IMAGE_TAG:-latest}
    container_name: ccframe-worker
    restart: unless-stopped
    env_file:
      - .env
    environment:
      NODE_ENV: production
      START_WORKERS: 'true'
      REDIS_URL: redis://redis:6379
      S3_ENDPOINT: http://minio:9000
    depends_on:
      db:
        condition: service_healthy
      redis:
        condition: service_started
      minio:
        condition: service_started
    command:
      - sh
      - -c
      - |
        set -e
        echo 'Applying database migrations for worker...'
        for i in $(seq 1 60); do npx prisma migrate deploy && break || (echo 'DB not ready, retry...' && sleep 2); done
        npx prisma generate
        node scripts/start-worker.js
    networks:
      - ccframe

  db:
    image: postgres:15-alpine
    container_name: ccframe-postgres
    restart: unless-stopped
    environment:
      POSTGRES_USER: ${POSTGRES_USER:-ccframe}
      POSTGRES_PASSWORD: ${POSTGRES_PASSWORD:-ccframe}
      POSTGRES_DB: ${POSTGRES_DB:-ccframe}
    volumes:
      - pgdata:/var/lib/postgresql/data
    networks:
      - ccframe
    healthcheck:
      test: ["CMD-SHELL", "pg_isready -U ${POSTGRES_USER:-ccframe} -d ${POSTGRES_DB:-ccframe}"]
      interval: 3s
      timeout: 3s
      retries: 20

  redis:
    image: redis:7-alpine
    container_name: ccframe-redis
    restart: unless-stopped
    networks:
      - ccframe

  minio:
    image: minio/minio:latest
    container_name: ccframe-minio
    restart: unless-stopped
    environment:
      MINIO_ROOT_USER: ${MINIO_ROOT_USER:-minioadmin}
      MINIO_ROOT_PASSWORD: ${MINIO_ROOT_PASSWORD:-minioadmin}
    command: server /data --console-address ":9001"
    ports:
      - "9000:9000"
      - "9001:9001"
    volumes:
      - minio:/data
    networks:
      - ccframe

  minio-init:
    image: minio/mc:latest
    depends_on:
      - minio
    environment:
      MINIO_ROOT_USER: ${MINIO_ROOT_USER:-minioadmin}
      MINIO_ROOT_PASSWORD: ${MINIO_ROOT_PASSWORD:-minioadmin}
      S3_BUCKET_NAME: ${S3_BUCKET_NAME:-ccframe}
      S3_ACCESS_KEY_ID: "${S3_ACCESS_KEY_ID:-}"
      S3_SECRET_ACCESS_KEY: "${S3_SECRET_ACCESS_KEY:-}"
    restart: "no"
    entrypoint: ["/bin/sh", "-c"]
    command: >-
      set -e;
      echo "Waiting for MinIO to be ready...";
      until mc alias set local http://minio:9000 "$${MINIO_ROOT_USER}" "$${MINIO_ROOT_PASSWORD}" 2>/dev/null; do
        echo "MinIO not ready, waiting...";
        sleep 2;
      done;
      echo "MinIO is ready!";
      SHOULD_CREATE_USER=0;
      if [ -n "$${S3_ACCESS_KEY_ID}" ] && [ -n "$${S3_SECRET_ACCESS_KEY}" ]; then
        if [ "$${S3_ACCESS_KEY_ID}" != "$${MINIO_ROOT_USER}" ] || [ "$${S3_SECRET_ACCESS_KEY}" != "$${MINIO_ROOT_PASSWORD}" ]; then
          SHOULD_CREATE_USER=1;
        fi;
      fi;
      if [ "$${SHOULD_CREATE_USER}" -eq 1 ]; then
        mc admin user add local "$${S3_ACCESS_KEY_ID}" "$${S3_SECRET_ACCESS_KEY}" || true;
        mc admin policy attach local readwrite --user "$${S3_ACCESS_KEY_ID}" || true;
      fi;
      BUCKET_NAME="$${S3_BUCKET_NAME:-ccframe}";
      mc mb -p "local/$${BUCKET_NAME}" || true;
      mc mb -p local/photo-gallery || true;
      echo 'MinIO buckets ensured';
    networks:
      - ccframe

  nginx:
    image: nginx:1.25-alpine
    container_name: ccframe-nginx
    restart: unless-stopped
    depends_on:
      web:
        condition: service_healthy
    ports:
      - "80:80"
    volumes:
      - ./nginx.conf:/etc/nginx/nginx.conf:ro
    networks:
      - ccframe
    healthcheck:
      test: ["CMD-SHELL", "wget -qO- http://localhost/nginx-health || exit 1"]
      interval: 30s
      timeout: 10s
      retries: 3
      start_period: 10s

volumes:
  pgdata:
  minio:

networks:
  ccframe:
    driver: bridge
EOF

  print_success "已生成镜像部署配置"
}

ensure_compose_integrity() {
  cd /opt/ccframe || return 1
  local f="docker-compose.yml"
  local FORCE="${1:-0}"
  if [ ! -f "$f" ]; then
    print_error "未找到 $f"
    return 1
  fi

  if [ "$FORCE" = "1" ]; then
    print_warning "使用 --fix-compose 强制覆盖 docker-compose.yml 为上游版本"
    curl -fsSL https://raw.githubusercontent.com/lonelyrower/CCFrame/main/docker-compose.yml -o "$f" || true
  fi

  sed -i 's/\r$//' "$f" 2>/dev/null || true
  awk 'NR==1{sub(/^[\xef\xbb\xbf]/,"")} {print}' "$f" > "$f.tmp" 2>/dev/null && mv "$f.tmp" "$f" || true

  if grep -qE '^(<<<<<<<|=======|>>>>>>>)[ ]' "$f"; then
    print_warning "检测到 docker-compose.yml 存在合并冲突，尝试恢复为上游版本"
    curl -fsSL https://raw.githubusercontent.com/lonelyrower/CCFrame/main/docker-compose.yml -o "$f" || true
  fi

  local svc_count
  svc_count=$(grep -n '^services:' "$f" | wc -l | tr -d ' ')
  if [ "$svc_count" -gt 1 ]; then
    local last_line
    last_line=$(grep -n '^services:' "$f" | tail -1 | cut -d: -f1)
    tail -n +"$last_line" "$f" > "$f.tmp" && mv "$f.tmp" "$f"
  fi

  if ! grep -q '/api/health-simple' "$f"; then
    sed -i 's#/api/health"#/api/health-simple"#g' "$f" 2>/dev/null || true
    sed -i 's#/api/health #/api/health-simple #g' "$f" 2>/dev/null || true
  fi

  if ! $DOCKER_COMPOSE_CMD -f "$f" config >/dev/null 2>&1; then
    print_warning "当前 docker-compose.yml 解析失败，回退为上游版本"
    curl -fsSL https://raw.githubusercontent.com/lonelyrower/CCFrame/main/docker-compose.yml -o "$f" || true
  fi

  if $DOCKER_COMPOSE_CMD -f "$f" config >/dev/null 2>&1; then
    print_success "docker-compose.yml 已修复并通过校验"
  else
    print_error "docker-compose.yml 仍无法解析，请手动检查"
  fi
}

ensure_env() {
  print_step "正在检查并生成环境变量..."

  if [ ! -f .env ]; then
    if [ -f .env.docker.example ]; then
      cp .env.docker.example .env
      print_success ".env 已从 .env.docker.example 复制"
    else
      print_warning "未找到 .env.docker.example，将基于默认模板生成 .env"

      local _ts=$(date +%s)
      local NEXTAUTH_SECRET ADMIN_PASSWORD POSTGRES_USER POSTGRES_PASSWORD POSTGRES_DB
      local S3_ACCESS_KEY_ID S3_SECRET_ACCESS_KEY MINIO_ROOT_USER MINIO_ROOT_PASSWORD
      NEXTAUTH_SECRET=$(openssl rand -base64 32 2>/dev/null || echo "change-me-${_ts}")
      ADMIN_PASSWORD=$(openssl rand -base64 12 2>/dev/null || echo "admin-${_ts}")
      POSTGRES_USER=ccframe
      POSTGRES_PASSWORD=$(openssl rand -base64 16 2>/dev/null || echo "db-${_ts}")
      POSTGRES_DB=ccframe
      S3_ACCESS_KEY_ID=$(openssl rand -base64 12 2>/dev/null || echo "s3-${_ts}")
      S3_SECRET_ACCESS_KEY=$(openssl rand -base64 16 2>/dev/null || echo "s3secret-${_ts}")
      MINIO_ROOT_USER=$(openssl rand -base64 12 2>/dev/null || echo "minio-${_ts}")
      MINIO_ROOT_PASSWORD=$(openssl rand -base64 16 2>/dev/null || echo "miniosecret-${_ts}")

      cat > .env <<EOF
NEXTAUTH_SECRET=${NEXTAUTH_SECRET}
ADMIN_EMAIL=admin@local.dev
ADMIN_PASSWORD=${ADMIN_PASSWORD}
POSTGRES_USER=${POSTGRES_USER}
POSTGRES_PASSWORD=${POSTGRES_PASSWORD}
POSTGRES_DB=${POSTGRES_DB}
DATABASE_URL=postgresql://${POSTGRES_USER}:${POSTGRES_PASSWORD}@db:5432/${POSTGRES_DB}
REDIS_URL=redis://redis:6379
S3_ACCESS_KEY_ID=${S3_ACCESS_KEY_ID}
S3_SECRET_ACCESS_KEY=${S3_SECRET_ACCESS_KEY}
S3_BUCKET_NAME=ccframe
S3_REGION=us-east-1
MINIO_ROOT_USER=${MINIO_ROOT_USER}
MINIO_ROOT_PASSWORD=${MINIO_ROOT_PASSWORD}
ENABLE_SEMANTIC_SEARCH=true
SEMANTIC_USE_PGVECTOR=off
EMBED_PROVIDER=openai
EMBED_MODEL_NAME=text-embedding-3-small
EMBED_DIM=1536
# 使用 OPENAI_API_KEY 指定 OpenAI API Key
EOF
    fi
  fi

  SERVER_IP=$(detect_server_ip)
  configure_deployment_target

  if [ -n "$ACCESS_BASE_URL" ]; then
    print_success "已将 NEXTAUTH_URL 设置为 $ACCESS_BASE_URL"
  else
    print_success "环境变量检查完成"
  fi
}

