#   bash ccframe.sh stop                     # 停止容器
#   bash ccframe.sh restart                  # 重启容器
#   bash ccframe.sh status                   # 查看容器状态
#   bash ccframe.sh logs [svc]               # 查看日志
#   bash ccframe.sh env                      # 生成或修复 .env
#   bash ccframe.sh health                   # 健康检查
#   bash ccframe.sh uninstall [--purge]      # 卸载
#   bash ccframe.sh switch-mode              # 切换部署模式

