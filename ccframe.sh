}

ensure_compose_https_mode() {
  if ! command -v python3 >/dev/null 2>&1; then
    print_warning "python3 未找到，跳过 docker-compose.yml 自动更新，请手动添加 443 端口和证书挂载"
    return
  fi
  python3 <<'PY2'
from pathlib import Path
path = Path("docker-compose.yml")
text = path.read_text()
if '      - "443:443"' not in text:
    text = text.replace('      - "80:80"\n', '      - "80:80"\n      - "443:443"\n', 1)
if '      - ./certbot:/var/www/certbot' not in text:
    text = text.replace('      - ./nginx.conf:/etc/nginx/nginx.conf:ro\n', '      - ./nginx.conf:/etc/nginx/nginx.conf:ro\n      - ./certbot:/var/www/certbot\n', 1)
if '      - /etc/letsencrypt:/etc/letsencrypt:ro' not in text:
    text = text.replace('      - ./nginx.conf:/etc/nginx/nginx.conf:ro\n', '      - ./nginx.conf:/etc/nginx/nginx.conf:ro\n      - /etc/letsencrypt:/etc/letsencrypt:ro\n', 1)
path.write_text(text)
PY2
}

ensure_compose_http_mode() {
  if ! command -v python3 >/dev/null 2>&1; then
    print_warning "python3 未找到，跳过 docker-compose.yml 自动修正，请手动移除 443 端口和证书挂载"
    return
  fi
  python3 <<'PY3'
from pathlib import Path
path = Path("docker-compose.yml")
lines = path.read_text().splitlines()
filtered = [line for line in lines if line.strip() not in {'- "443:443"', '- /etc/letsencrypt:/etc/letsencrypt:ro', '- ./certbot:/var/www/certbot'}]
if filtered and filtered[-1] != '':
    filtered.append('')
path.write_text("\n".join(filtered))
PY3
}

generate_nginx_http_conf() {
  local domain="$1"
  local server_names="_"
  if [ -n "$domain" ]; then
    server_names="$domain _"
  elif [ -n "$SERVER_IP" ]; then
    server_names="$SERVER_IP _"
  fi

  cat > nginx.conf <<EOF
# Generated by ccframe.sh (mode: http)
events {
  worker_connections 1024;
}

http {
  include /etc/nginx/mime.types;
  default_type application/octet-stream;

  sendfile on;
  keepalive_timeout 65;

  resolver 127.0.0.11 valid=30s;

  upstream web_backend {
    server web:3000;
  }

  server {
    listen 80 default_server;
    server_name $server_names;

    client_max_body_size 50m;

    location /nginx-health {
      access_log off;
      return 200 "healthy\n";
      add_header Content-Type text/plain;
    }

    location / {
      proxy_pass http://web_backend;
      proxy_http_version 1.1;
      proxy_set_header Host \$host;
      proxy_set_header X-Real-IP \$remote_addr;
      proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
      proxy_set_header X-Forwarded-Proto \$scheme;
      proxy_set_header Upgrade \$http_upgrade;
      proxy_set_header Connection "upgrade";

      proxy_connect_timeout 60s;
      proxy_send_timeout 300s;
      proxy_read_timeout 300s;

      proxy_next_upstream error timeout invalid_header http_500 http_502 http_503 http_504;
      proxy_next_upstream_tries 3;
      proxy_next_upstream_timeout 30s;
    }
  }
}
EOF
}

generate_nginx_https_conf() {
  local domain="$1"
  local server_names="$domain"
  if [ -z "$domain" ]; then
    print_error "域名为空，无法生成 HTTPS 配置"
    exit 1
  fi

  cat > nginx.conf <<EOF
# Generated by ccframe.sh (mode: https)
events {
  worker_connections 1024;
}

http {
  include /etc/nginx/mime.types;
  default_type application/octet-stream;

  sendfile on;
  keepalive_timeout 65;

  resolver 127.0.0.11 valid=30s;

  upstream web_backend {
    server web:3000;
  }

  server {
    listen 80;
    server_name $server_names;

    client_max_body_size 50m;

    location /nginx-health {
      access_log off;
      return 200 "healthy\n";
      add_header Content-Type text/plain;
    }

    location / {
      return 301 https://\$host\$request_uri;
    }
  }

  server {
    listen 443 ssl http2;
    server_name $server_names;

    ssl_certificate /etc/letsencrypt/live/$domain/fullchain.pem;
    ssl_certificate_key /etc/letsencrypt/live/$domain/privkey.pem;
EOF

  if [ -f /etc/letsencrypt/options-ssl-nginx.conf ]; then
    printf '    include /etc/letsencrypt/options-ssl-nginx.conf;\n' >> nginx.conf
  fi
  if [ -f /etc/letsencrypt/ssl-dhparams.pem ]; then
    printf '    ssl_dhparam /etc/letsencrypt/ssl-dhparams.pem;\n' >> nginx.conf
  fi

  cat <<'EOF' >> nginx.conf
    client_max_body_size 50m;

    location / {
      proxy_pass http://web_backend;
      proxy_http_version 1.1;
      proxy_set_header Host $host;
      proxy_set_header X-Real-IP $remote_addr;
      proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
      proxy_set_header X-Forwarded-Proto $scheme;
      proxy_set_header Upgrade $http_upgrade;
      proxy_set_header Connection "upgrade";

      proxy_connect_timeout 60s;
      proxy_send_timeout 300s;
      proxy_read_timeout 300s;

      proxy_next_upstream error timeout invalid_header http_500 http_502 http_503 http_504;
      proxy_next_upstream_tries 3;
      proxy_next_upstream_timeout 30s;
    }
  }
}
EOF
}

obtain_letsencrypt_certificate() {
  local domain="$1"
  local email="$2"

  if [ -d "/etc/letsencrypt/live/$domain" ]; then
    print_info "检测到现有证书 /etc/letsencrypt/live/$domain"
    return
  fi

  print_step "申请 Let's Encrypt 证书 ($domain)..."
  apt-get update -y >/dev/null 2>&1 || true
  apt-get install -y certbot >/dev/null 2>&1 || true

  systemctl stop nginx 2>/dev/null || service nginx stop 2>/dev/null || true
  docker stop ccframe-nginx >/dev/null 2>&1 || true

  if certbot certonly --standalone --agree-tos --no-eff-email -m "$email" -d "$domain"; then
    print_success "证书申请成功"
  else
    print_error "证书申请失败，请确认域名解析已经指向本服务器"
    exit 1
  fi
}

configure_certbot_cron() {
  local cron_file="/etc/cron.d/ccframe-certbot"
  cat > "$cron_file" <<'EOF'
SHELL=/bin/bash
0 3 * * * root certbot renew --standalone --pre-hook "docker stop ccframe-nginx >/dev/null 2>&1 || true" --post-hook "docker start ccframe-nginx >/dev/null 2>&1 || true" >> /var/log/ccframe-certbot.log 2>&1
EOF
  chmod 644 "$cron_file" >/dev/null 2>&1 || true
  print_success "已写入证书自动续期任务 (/etc/cron.d/ccframe-certbot)"
}

configure_ip_mode() {
  DEPLOYMENT_MODE="ip"
  SERVER_IP=${SERVER_IP:-$(detect_server_ip)}
  ACCESS_BASE_URL="http://$SERVER_IP"
  set_env_value "DEPLOYMENT_MODE" "$DEPLOYMENT_MODE"
  set_env_value "NEXTAUTH_URL" "$ACCESS_BASE_URL"
  set_env_value "FORCE_HTTPS" "false"
  set_env_value "APP_DOMAIN" ""
  set_env_value "SSL_EMAIL" ""
  ensure_compose_http_mode
  generate_nginx_http_conf ""
  print_info "当前使用 IP 部署模式 (HTTP): $ACCESS_BASE_URL"
}

configure_letsencrypt_mode() {
  DEPLOYMENT_MODE="letsencrypt"
  local domain=$(get_env_value APP_DOMAIN)
  local email=$(get_env_value SSL_EMAIL)

  if [ -z "$domain" ]; then
    if is_interactive; then
      read -rp "请输入绑定的域名: " domain
      while [ -z "$domain" ]; do
        print_warning "域名不能为空"
        read -rp "请输入绑定的域名: " domain
      done
    else
      print_error "未配置 APP_DOMAIN，无法在非交互模式下启用 HTTPS"
      exit 1
    fi
  fi

  if [ -z "$email" ]; then
    if is_interactive; then
      read -rp "请输入用于 Let's Encrypt 的邮箱: " email
      while [ -z "$email" ]; do
        print_warning "邮箱不能为空"
        read -rp "请输入用于 Let's Encrypt 的邮箱: " email
      done
    else
      print_error "未配置 SSL_EMAIL，无法在非交互模式下启用 HTTPS"
      exit 1
    fi
  fi

  set_env_value "DEPLOYMENT_MODE" "$DEPLOYMENT_MODE"
  set_env_value "APP_DOMAIN" "$domain"
  set_env_value "SSL_EMAIL" "$email"
  set_env_value "FORCE_HTTPS" "true"

  ACCESS_BASE_URL="https://$domain"
  set_env_value "NEXTAUTH_URL" "$ACCESS_BASE_URL"

  ensure_certbot_webroot
  obtain_letsencrypt_certificate "$domain" "$email"
  ensure_compose_https_mode
  generate_nginx_https_conf "$domain"
  configure_certbot_cron
  print_info "已配置域名 HTTPS 模式: $ACCESS_BASE_URL"
}

